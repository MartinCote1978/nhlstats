#' nhlstats: Collection of NHL statistics for analysis with R
#' 
#' nhlstats is an R package containing a multitude of hockey player and 
#' team statistics for the National Hockey League as well as other
#' leaagues from 1917 through 2013. The data frames in this package are
#' derived from the following sources:
#' 
#' \itemize{
#'   \item The Hockey Databank: A comprehensive database of hockey statistics
#'   stored as separate tables in a relational database framework inspired by
#'   the Lehman baseball database.  Membership to the hockey-databank Yahoo
#'   group at \url{http://sports.groups.yahoo.com/group/hockey-databank} is
#'   required to access the raw database files.  
#'   \item \href{http://www.hockey-reference.com/}{Hockey-Reference.com}: 
#'   Online hockey statistics updated in real time with statistics at the
#'   league, season, franchise, player, and game level.  In addition, functions
#'   have been included to scrape the site to update the included data frame
#'   with new records so users can obtain the most up-to-date statistics.
#'   \item \href{http://www.behindthenet.ca/}{Behindthenet.ca} Online source
#'   of advanced hockey statistics at the player and team levels from the 
#'   2007-08 through 2013-2014 seasons. 
#' }
#' @name nhlstats
#' @docType package
